        resetRenderer.setFont(new Font("Arial", Font.BOLD, 12));
        resetRenderer.setForeground(Color.BLACK);

        if (tb == tableDetailBill) {
            tableDetailBill.getColumn("M√£ s·∫£n ph·∫©m").setCellRenderer(resetRenderer);
            tableDetailBill.getColumn("Gi√° b√°n").setCellRenderer(resetRenderer);
            tableDetailBill.getColumn("S·ªë l∆∞·ª£ng").setCellRenderer(resetRenderer);
            tableDetailBill.getColumn("Th√†nh ti·ªÅn").setCellRenderer(resetRenderer);
        }

